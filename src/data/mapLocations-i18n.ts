// Função para obter localização traduzida
export const getTranslatedLocation = (slug: string, index: number, lang: 'pt' | 'en' | 'es') => {
  const packageTranslations = mapLocationsTranslations[slug as keyof typeof mapLocationsTranslations];
  if (!packageTranslations) return null;
  
  const location = packageTranslations.locations[index];
  if (!location) return null;
  
  return {
    name: location.name[lang],
    description: location.description[lang]
  };
};

// Função para obter todas as localizações traduzidas de um pacote
export const getAllTranslatedLocations = (slug: string, lang: 'pt' | 'en' | 'es') => {
  const packageTranslations = mapLocationsTranslations[slug as keyof typeof mapLocationsTranslations];
  if (!packageTranslations) return [];
  
  return packageTranslations.locations.map(location => ({
    name: location.name[lang],
    description: location.description[lang]
  }));
};